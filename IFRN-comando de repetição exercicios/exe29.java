
/*Escreva um algoritmo que calcule a soma dos números de 1 a 15. Utilize o comando de repetição Repita
(Do...While). */

public class exe29 {
     
     public static void main  (String[]args ){

          int soma = 0;
          int i = 1;
          
          do {
               


               i++;
               
               soma = soma + i;
               
               System.out.println(" A SOMA DOS NÚMEROS DE 1 A 15 É " + soma);



          }while ( i  <= 15);




     }
}
